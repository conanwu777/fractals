#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*(char*)s)
	{
		if (*(char*)s == (char)c)
			return ((char*)s);
		s++;
	}
	if ((char)c == '\0')
		return ((char*)s);
	return (NULL);
}
