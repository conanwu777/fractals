#include "libft.h"

char	*ft_itoa(int n)
{
	char *str;
	char *t;
	char *u;

	if (!(str = (char *)malloc(16)))
		return (NULL);
	t = str;
	(n < 0 ? *t++ = '-' : 1);
	if (n > 0)
		n = -n;
	if (n <= -10)
	{
		u = ft_itoa(-(n / 10));
		while (*u)
			*t++ = *u++;
	}
	*t = '0' - n % 10;
	*(t + 1) = '\0';
	return (str);
}
