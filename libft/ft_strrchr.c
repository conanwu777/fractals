#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *t;

	t = (char*)s;
	while (*t)
		t++;
	if ((char)c == '\0')
		return (t);
	while (t >= s)
	{
		if (*t == (char)c)
			return (t);
		t--;
	}
	return (NULL);
}
