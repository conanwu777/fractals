#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	(nb < 0 ? ft_putchar_fd('-', fd) : 1);
	if (nb > 0)
		nb = -nb;
	(nb <= -10 ? ft_putnbr_fd(-(nb / 10), fd) : 1);
	ft_putchar_fd('0' - nb % 10, fd);
}
