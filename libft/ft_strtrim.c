#include "libft.h"

char	*ft_strtrim(char const *s)
{
	char *t;
	char *u;

	while (s && *s && (*s == ' ' || *s == '\n' || *s == '\t'))
		s++;
	if (!s || !(t = (char*)malloc(ft_strlen(s) + 1)))
		return (NULL);
	u = t;
	while (*s)
		*u++ = *s++;
	*u = '\0';
	u--;
	while (*u == ' ' || *u == '\n' || *u == '\t')
		*u-- = '\0';
	return (t);
}
