#include "libft.h"

int	ft_atoi(const char *str)
{
	int sum;
	int sign;

	sum = 0;
	sign = 1;
	while ((*str == ' ') || (*str == '\t') || (*str == '\v') ||
	(*str == '\n') || (*str == '\f') || (*str == '\r'))
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		sum = sum * 10 + sign * (*str++ - '0');
	return (sum);
}
