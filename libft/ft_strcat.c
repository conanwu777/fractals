#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	char *t;

	t = s1;
	while (*t)
		t++;
	while (*s2)
		*t++ = *s2++;
	*t = '\0';
	return (s1);
}
